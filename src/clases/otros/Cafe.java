package clases.otros;

public class Cafe {
    private String nombre;
    private int gCafe;
    private int gLeche;
    private int gCacao;

    public Cafe(String nombre, int gCafe, int gLeche, int gCacao) {
        this.nombre = nombre;
        this.gCafe = gCafe;
        this.gLeche = gLeche;
        this.gCacao = gCacao;
    }
}
